// --- Keyboard shortcut: spacebar toggles play/pause ---
document.addEventListener("keydown", (e) => {
  if (e.code !== "Space") return;

  const tag = document.activeElement.tagName;
  const isTypingOrScrubbing =
    tag === "INPUT" || tag === "TEXTAREA" || document.activeElement.isContentEditable;

  if (isTypingOrScrubbing) return; // let space work normally in search box / sliders

  e.preventDefault(); // stop the page itself from scrolling
  isPlaying ? pauseSong() : playSong();
});

audio.addEventListener("loadedmetadata", () => {
  durationEl.textContent = formatTime(audio.duration);
});

audio.addEventListener("error", () => {
  const song = songs[songIndex];
  console.error("Audio error on track:", song ? song.name : "(unknown)", audio.error);
  pauseSong();
  alert(
    `"${song ? song.name : "This track"}" couldn't be played. ` +
    `The file format may not be supported by your browser, or the file may be corrupted.`
  );
});

audio.addEventListener("timeupdate", () => {
  if (audio.duration) {
    const percent = (audio.currentTime / audio.duration) * 100;
    progress.value = percent;
    currentTimeEl.textContent = formatTime(audio.currentTime);
    updateProgressFill(percent);
  }
});

audio.addEventListener("ended", () => {
  if (repeatMode === "one") {
    audio.currentTime = 0;
    playSong();
    return;
  }

  const atEnd = isShuffle
    ? shufflePos === shuffleOrder.length - 1
    : songIndex === songs.length - 1;

  if (atEnd && repeatMode === "off") {
    pauseSong();
    audio.currentTime = 0;
    return;
  }

  nextSong();
});

progress.addEventListener("input", () => {
  updateProgressFill(progress.value);
  if (audio.duration) {
    audio.currentTime = (progress.value / 100) * audio.duration;
  }
});

volume.addEventListener("input", () => {
  audio.volume = volume.value;
});

