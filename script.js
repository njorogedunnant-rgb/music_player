// --- IndexedDB storage for tracks added via the UI ---
// (these live in the browser, separate from songs/playlist.json,
// so tracks you add here persist across reloads without touching the terminal)
const DB_NAME = "musicPlayerDB";
const DB_VERSION = 1;
const STORE_NAME = "customTracks";

function openTracksDB() {
  return new Promise((resolve, reject) => {
    const req = indexedDB.open(DB_NAME, DB_VERSION);
    req.onupgradeneeded = () => {
      const db = req.result;
      if (!db.objectStoreNames.contains(STORE_NAME)) {
        db.createObjectStore(STORE_NAME, { keyPath: "id", autoIncrement: true });
      }
    };
    req.onsuccess = () => resolve(req.result);
    req.onerror = () => reject(req.error);
  });
}

async function saveCustomTrack(file, name, artistName) {
  const db = await openTracksDB();
  return new Promise((resolve, reject) => {
    const tx = db.transaction(STORE_NAME, "readwrite");
    const store = tx.objectStore(STORE_NAME);
    const req = store.add({ name, artist: artistName, blob: file, addedAt: Date.now() });
    req.onsuccess = () => resolve(req.result);
    req.onerror = () => reject(req.error);
  });
}

async function getAllCustomTracks() {
  const db = await openTracksDB();
  return new Promise((resolve, reject) => {
    const tx = db.transaction(STORE_NAME, "readonly");
    const store = tx.objectStore(STORE_NAME);
    const req = store.getAll();
    req.onsuccess = () => resolve(req.result);
    req.onerror = () => reject(req.error);
  });
}

async function deleteCustomTrack(id) {
  const db = await openTracksDB();
  return new Promise((resolve, reject) => {
    const tx = db.transaction(STORE_NAME, "readwrite");
    const store = tx.objectStore(STORE_NAME);
    const req = store.delete(id);
    req.onsuccess = () => resolve();
    req.onerror = () => reject(req.error);
  });
}

// Best-effort "Artist - Title" parsing from a picked filename
function guessTitleArtist(filename) {
  let name = filename.replace(/\.[^/.]+$/, ""); // strip extension
  name = name.replace(/_+/g, " ").trim();
  const parts = name.split(/\s+-\s+/);
  if (parts.length === 2 && parts[0].length < 40) {
    return { artistName: parts[0].trim(), trackName: parts[1].trim() };
  }
  return { artistName: "Unknown Artist", trackName: name };
}

// Songs are loaded from songs/playlist.json, which is generated by
// running: python3 build_playlist.py
// (that script copies your mp3s from ~/Music into songs/ and writes
// the json file automatically based on what it finds)
let songs = [];
let songIndex = 0;
let isPlaying = false;

// Shuffle state
let isShuffle = false;
let shuffleOrder = [];
let shufflePos = 0;

// Repeat state: "off" -> "all" -> "one" -> "off" ...
let repeatMode = "off";

async function loadPlaylist() {
  try {
    const res = await fetch("songs/playlist.json");
    songs = await res.json();
  } catch (err) {
    console.warn("Could not load songs/playlist.json, using fallback song list.", err);
    songs = [
      { name: "Song Title 1", artist: "Artist 1", src: "songs/song1.mp3", cover: "images/cover.jpg" }
    ];
  }

  // Append any tracks previously added through the "+ Add" button
  try {
    const customTracks = await getAllCustomTracks();
    customTracks.forEach(t => {
      songs.push({
        name: t.name,
        artist: t.artist,
        src: URL.createObjectURL(t.blob),
        cover: "images/cover.jpg",
        isCustom: true,
        dbId: t.id
      });
    });
  } catch (err) {
    console.warn("Could not load tracks added via the UI:", err);
  }

  renderTrackList();
  loadSong(songIndex);
}

function loadSong(index) {
  if (!songs.length) return;
  const song = songs[index];
  title.textContent = song.name;
  artist.textContent = song.artist;
  cover.src = song.cover;
  audio.src = song.src;
  progress.value = 0;
  currentTimeEl.textContent = "0:00";
  updateProgressFill(0);
  highlightActiveTrack();
}

function initAudioGraph() {
  if (audioCtx) return; // already set up, only do this once per page load
  try {
    audioCtx = new (window.AudioContext || window.webkitAudioContext)();
    sourceNode = audioCtx.createMediaElementSource(audio);

    eqFilters = EQ_BANDS.map(freq => {
      const filter = audioCtx.createBiquadFilter();
      filter.type = "peaking";
      filter.frequency.value = freq;
      filter.Q.value = 1;
      filter.gain.value = 0;
      return filter;
    });

    analyser = audioCtx.createAnalyser();
    analyser.fftSize = 64;
    vizDataArray = new Uint8Array(analyser.frequencyBinCount);

    // Chain: source -> filter1 -> filter2 -> ... -> analyser -> speakers
    let node = sourceNode;
    eqFilters.forEach(f => { node.connect(f); node = f; });
    node.connect(analyser);
    analyser.connect(audioCtx.destination);

    drawVisualizer();
  } catch (err) {
    console.warn("Web Audio EQ/visualizer unavailable in this browser:", err);
  }
}

function playSong() {
  initAudioGraph();
  if (audioCtx && audioCtx.state === "suspended") {
    audioCtx.resume();
  }
  const playPromise = audio.play();
  if (playPromise && playPromise.catch) {
    playPromise.catch(err => {
      console.error("Playback failed:", err);
      alert("This track couldn't be played. The file format may not be supported by your browser.");
    });
  }
  playBtn.innerHTML = "&#10074;&#10074;"; // pause icon ||
  isPlaying = true;
}

function pauseSong() {
  audio.pause();
  playBtn.innerHTML = "&#9658;"; // play icon ▶
  isPlaying = false;
}

function generateShuffleOrder() {
  shuffleOrder = songs.map((_, i) => i);
  // Fisher-Yates shuffle
  for (let i = shuffleOrder.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffleOrder[i], shuffleOrder[j]] = [shuffleOrder[j], shuffleOrder[i]];
  }
  // Keep the currently playing song as the shuffle "starting point"
  // so toggling shuffle mid-playback doesn't jump elsewhere.
  const curPos = shuffleOrder.indexOf(songIndex);
  [shuffleOrder[0], shuffleOrder[curPos]] = [shuffleOrder[curPos], shuffleOrder[0]];
  shufflePos = 0;
}

function nextSong() {
  if (isShuffle) {
    shufflePos = (shufflePos + 1) % shuffleOrder.length;
    songIndex = shuffleOrder[shufflePos];
  } else {
    songIndex = (songIndex + 1) % songs.length;
  }
  loadSong(songIndex);
  playSong();
}

function prevSong() {
  if (isShuffle) {
    shufflePos = (shufflePos - 1 + shuffleOrder.length) % shuffleOrder.length;
    songIndex = shuffleOrder[shufflePos];
  } else {
    songIndex = (songIndex - 1 + songs.length) % songs.length;
  }
  loadSong(songIndex);
  playSong();
}

function playTrackAt(index) {
  songIndex = index;
  if (isShuffle) {
    // realign shuffle position so next/prev continue sensibly from here
    const pos = shuffleOrder.indexOf(index);
    shufflePos = pos === -1 ? 0 : pos;
  }
  loadSong(songIndex);
  playSong();
}

function formatTime(seconds) {
  if (isNaN(seconds)) return "0:00";
  const mins = Math.floor(seconds / 60);
  const secs = Math.floor(seconds % 60).toString().padStart(2, "0");
  return `${mins}:${secs}`;
}

function fillRange(rangeEl, percent) {
  const p = Math.min(Math.max(percent, 0), 100);
  rangeEl.style.background =
    `linear-gradient(to right, #6a3de8 0%, #6a3de8 ${p}%, #33343d ${p}%, #33343d 100%)`;
}

function seekBy(seconds) {
  if (!audio.duration) return;
  audio.currentTime = Math.min(
    Math.max(audio.currentTime + seconds, 0),
    audio.duration
  );
}

