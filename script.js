eqSliders.forEach(slider => {
  const valueLabel = slider.nextElementSibling; // .eq-value span right after the slider

  slider.addEventListener("input", () => {
    const freq = Number(slider.dataset.freq);
    const filter = eqFilters.find(f => f.frequency.value === freq);
    if (filter) filter.gain.value = Number(slider.value);
    const val = Number(slider.value);
    valueLabel.textContent = val > 0 ? `+${val}` : `${val}`;
  });
});

