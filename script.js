// --- Add tracks from your device ---
addTrackBtn.addEventListener("click", () => addTrackInput.click());

addTrackInput.addEventListener("change", async (e) => {
  const files = Array.from(e.target.files);

  for (const file of files) {
    const { trackName, artistName } = guessTitleArtist(file.name);
    try {
      const id = await saveCustomTrack(file, trackName, artistName);
      songs.push({
        name: trackName,
        artist: artistName,
        src: URL.createObjectURL(file),
        cover: "images/cover.jpg",
        isCustom: true,
        dbId: id
      });
    } catch (err) {
      console.warn("Failed to save track:", file.name, err);
    }
  }

  if (isShuffle) generateShuffleOrder();
  renderTrackList(trackSearch.value);

  // Reset so selecting the same file again still triggers "change"
  e.target.value = "";
});

